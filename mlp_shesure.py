class PCOSDetector(nn.Module):
    """
    Multi-Layer Perceptron for PCOS Binary Classification.

    Architecture:
        Input → FC(256) → BN → ReLU → Dropout
              → FC(128) → BN → ReLU → Dropout
              → FC(64)  → BN → ReLU → Dropout
              → FC(32)  → BN → ReLU → Dropout
              → FC(1)   → Sigmoid
    """
    def __init__(self, input_dim, hidden_dims=[256, 128, 64, 32],
                 dropout_rate=0.3):
        super(PCOSDetector, self).__init__()

        self.input_dim   = input_dim
        self.hidden_dims = hidden_dims
        self.dropout_rate = dropout_rate

        layers = []
        in_dim = input_dim

        for i, out_dim in enumerate(hidden_dims):
            layers.append(nn.Linear(in_dim, out_dim))
            layers.append(nn.BatchNorm1d(out_dim))
            layers.append(nn.ReLU())
            layers.append(nn.Dropout(p=dropout_rate))
            in_dim = out_dim

        layers.append(nn.Linear(in_dim, 1))
        layers.append(nn.Sigmoid())

        self.network = nn.Sequential(*layers)
        self._init_weights()

    def _init_weights(self):
        for layer in self.modules():
            if isinstance(layer, nn.Linear):
                nn.init.kaiming_normal_(layer.weight, nonlinearity='relu')
                nn.init.zeros_(layer.bias)

    def forward(self, x):
        return self.network(x).squeeze(1)

    def count_parameters(self):
        return sum(p.numel() for p in self.parameters() if p.requires_grad)


