def cap_outliers_iqr(df, cols, factor=3.0):
    """Cap outliers using IQR method."""
    df = df.copy()
    for col in cols:
        Q1 = df[col].quantile(0.25)
        Q3 = df[col].quantile(0.75)
        IQR = Q3 - Q1
        lower = Q1 - factor * IQR
        upper = Q3 + factor * IQR
        df[col] = df[col].clip(lower=lower, upper=upper)
    return df

